          <p>Nakon što se obavi izrada web sajta, da bi taj isti veb sajt bio vidljiv na internetu potrebno je da se locira na veb server. Često u žargonu možete čuti da je neko "digao" sajt. To zapravo znači da je smešten na veb server.</p>

          <p>Mnogo ljudi zna kako se vozi auto, ali imaju malo ili nemaju uopšte znanja o tome kako auto zapravo radi. Tako takođe, mnogo ljudi zna kako pregledati web stranice, ali imaju limitirano znanje o tome kako stranice rade to što oni rade.</p>

          <p>Sa aspekta računarskih mreža, najpopularnija arhitektura koja se koristi prilikom izrade sajtova i web aplikacija je klijent-server arhitektura. Kada želimo da odemo na neki sajt, npr. sajt facebook-a,
           naš pretraživač koji koristimo npr. Google Chrome ima ulogu klijenta, dok računar na kome je smeštena stranica facebook-a ima ulogu servera.</p>

          <p>Web server je softver i hardver koji je zaslužan za rad određenog softvera, koji podržava World Wide Web klijentske zahteve. Može sadržati jedan ili više web sajtova. Primarna funkcija web servera je smeštanje, procesiranje i slanje web stranica klijentu, odnosno našem browseru.</p>

          <p><strong>Kako sve to?</strong></p>

          <p>Svaka web stranica na internetu ima jedinstvenu adresu, koja se naziva
          Uniform Resource Locator ili skraćeno URL. Nešto poput "www.sajt.com/page1.html" je primer tipičnog URL-a. Kada korisnik napiše ovu adresu u pretraživač, računar na kojoj radi pretraživač šalje zahtev na IP adresu računara koji je veb server za tu stranicu, zahtevajući od njega da pošalje sav sadržaj nazad. Jednom kada web server primi taj zahtev, on šalje sadržaj stranice nazad na IP adresu računara koji je taj zahtev poslao. Pretraživač potom prevodi taj sadržaj u tekst, slike, linkove, video itd. i ostali web sadržaj. </p>

          <p><strong>Treba napomenuti da su web serveri hardverski moćni računari, jer moraju da se izbore sa zahtevima ogromnog broja klijenata.</strong></p>
