          <p><strong>Kriptografija je nauka koja se bavi metodologijom očuvanja tajnosti informacija. Zabranjene ljubavne veze, komunikacija u toku rata i poslovne transakcije su samo neke od ljudskih potreba koje zahtevaju bezbedno komuniciranje i primenu kriptografije.</strong></p>
          <p>Kriptografske tehnike dozvoljavaju pošiljaocu da maskira podatke tako da uljez ne može da dobije nikakvu informaciju iz presretnutih podataka. Primalac sa druge strane mora da bude u stanju da izvuče originalne podatke iz maskiranih podataka. Na primer, neka Mina šalje Ivanu poruku u svom originalnom obliku - "Ivane, dođi u 12 časova. Mina". Ova poruka je poznata kao otvoren tekst ( plaintext ) ili jasan tekst ( cleartext ). Mina šifruje ovu poruku u otvorenom tekstu koristeći algoritam šifrovanja tako da šifrovana poruka, koja je poznata još i pod imenom šifrovan tekst ( ciphertext ) svakom uljezu izgleda nerazumljiv. Postavlja se pitanje kako samo Ivan bude taj koji dešifruje podatke koji se prenose. Ovde na scenu stupaju ključevi.</p>
          <p>Ivan poseduje svoj ključ koji u kombinaciji sa algoritmom za dešifrovanje uzima šifrovan tekst i od njega pravi originalan otvoren tekst. I eto, Ivan je u stanju da pročita poruku koju mu je poslala Mina. Što se tiče ključeva, treba još napomenuti da u <strong>sistemima simetričnih ključeva</strong>, ključevi su tajni. Dok u <strong>sistemima javnih ključeva</strong>, koristi se par ključeva. Jedan par ključeva znaju Ivan i Mina ( zapravo, zna ih ceo svet ). Drugi ključ znaju ili Ivan ili Mina ( ali ne oboje ).
          </p>
          <p>
           Ako hoćemo jednostavnim primerom da objasnimo šta kriptografija predstavlja, bilo to ovako: Mina želi da samo Ivan može da razume poruku koju je ona poslala, i pored toga što oni komuniciraju putem interneta na kojem neki uljez može da presretne sve što se prenosi od Mine do Ivana. Ivan želi da bude siguran da je poruku koju prima od Mine zapravo ona poslala, a Mina želi da bude sigurna da je osoba sa kojom komunicira zaista Ivan. Oboje žele da budu sigurni da sadržaj njihovih poruka nije izmenjen tokom prenosa.</p>
