          <p><strong>WWW</strong></p>
          <p>Internet pruža veliki broj različitih usluga i servisa od kojih je najčešće korišćena WWW - <strong>World Wide Web</strong>. WWW je zapravo komunikacioni protokol koji omogućava da se tekst, slika, zvuk sa interneta prikazuju u našem programu koji se naziva brauzer. Mnogi korisnici pogrešno poistovećuju celokupan internet sa WWW servisom. Razlika je u tome što je internet celokupna svetska mreža sa svojim hardverskim i softverskim komponentama, dok je WWW samo jedan servis kojim se komunicira preko interneta. Ali zapravo najčešće korišćen.</p>
          <p><strong>TCP/IP</strong></p>
          <p>Protokol preko koga se prenose bilo kakvi podaci na internetu naziva se TCP/IP protokol ( Transmition Control Protocol/Internet Protocol). On je osnovni protokol za komuniciranje internetom. Njegova najvažnija uloga je neposredno obezbeđivanje komunikacije procesima aplikacija koje se izvršavaju na različitim računarima.</p>
          <p><strong>HTTP</strong></p>
          <p>HTTP - <strong>HyperText Transfer Protocol</strong> je oznaka za način komunikacije pretraživača i servera putem internet stranica koje mogu sadržati raznovrsne tipove podataka kao što su tekst, slike,
              muzika i video... Ovaj protokol je najčešće korišćen protokol na internetu. HTTP protokol definiše način na koji veb klijenti traže veb strane od veb servera, kao i način na koji veb serveri šalju tražene strane klijentima. Na primer, kada korisnik zatraži neku veb stranicu klikom na neki link, pretraživač šalje serveru HTTP poruke sa zahtevom za objekte sa odgovarajuće strane. Za HTTP protokol se često u žargonu kaže da je srce veba.</p>
          <p><strong>Internet adresa</strong></p>
          <p>Kao što u svakodnevnom životu moramo znati naziv ulice da bi došli do nekog mesta, tako se mora znati i internet adresa nekog sajta da bi se do nje došlo. Na primer imamo adresu: </p>
          <p><strong>http://www.nekisajt.rs</strong></p>
          <p>Ovako zapisana adresa predstavlja jedinstveni URL - <strong>Uniform Resorce Locator</strong>. Pomoću ove adrese se na internetu lociraju svi podaci. URL adresa se sastoji od oznake protokola <strong>http:</strong>, od oznake servisa <strong>www</strong>, imena sajta <strong>nekisajt</strong>, od oznake domena <strong>.rs</strong>. Oznaka domena zapravo predstavlja informaciju gde je sajt fizički lociran. Značajno je i pomenuti i šta raditi ako se ne poznaje tačna internet adresa nekog sajta. Rešenje ovog problema leži u internet pretraživačkim mehanizmima ( search engine ) koji na osnovu unetih pojmova traže željene veb sajtove.</p>
