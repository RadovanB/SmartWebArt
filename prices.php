 // TypeWriter START
 class TypeWriter {
  constructor(txtElement, wait = 3000) {
    this.txtElement = txtElement;
    this.txt = '';
    this.wait = parseInt(wait, 10);
    this.type();
    this.isDeleting = false;
  }

  type() {

    const fullTxt = this.txtElement;
     
    // Check if deleting
    if(this.isDeleting) {
      // Remove char
      this.txt = this.txtElement.substring(0, fullTxt.length - 1);
    }
     else {
      // Add char
      this.txt = fullTxt.substring(0, fullTxt.length + 1);
    }

    // Insert txt into element
    this.txtElement = this.txt;
    // console.log(this.txtElement);
    document.querySelector('.page-title').textContent = this.txt;
    
    // Initial Type Speed
    let typeSpeed = 300;

    if(this.isDeleting) {
      typeSpeed /= 2;
    }

    // If word is complete
    if(!this.isDeleting && this.txt === fullTxt) {
      // Make pause at end
      typeSpeed = this.wait;
      // Set delete to true
      this.isDeleting = true;
    } else if(fullTxt === 'K') {
      document.querySelector('.page-title').textContent = "Kvalitetan sajt po povoljnim cenama";
      this.isDeleting = true;
      this.txtElement = "Kvalitetan sajt po povoljnim cenama";
      // Pause before start typing
      typeSpeed = 500;
    }

    setTimeout(() => this.type(), typeSpeed);
  }
}

